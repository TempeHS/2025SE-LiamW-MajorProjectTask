while running:
    # poll for events
    for event in pygame.event.get():
        mx, my = pygame.mouse.get_pos()  # gets mouse x,y coordinates
        location = [mx, my]
        if event.type == pygame.QUIT:
            running = False
        if event.type == pygame.MOUSEBUTTONDOWN:
            #all mouse inputs
            if event.button == 1:  
                for units in unitlist:
                    units.create_path()
                for structures in structurelist:
                    structures.create_path()
                for worker in workerlist:
                    worker.create_path()
        if event.type == pygame.KEYDOWN:
            #all keyboard inputs
            #queue
            if event.key == pygame.K_b:
                for structures in structurelist:
                    structures.startqueue()
            if event.key == pygame.K_c:
                for structures in structurelist:
                    structures.stopqueue()

    # fill the screen with a color to wipe away anything from last frame
    #screen.blit(bg_surf,(0,0))
    cameralist.custom_draw()
    workerlist.update(screen,resourcelist,structurelist)
    unitlist.update(screen)
    resourcelist.update(screen)
    structurelist.update(screen,dt,Map)

    for structure in structurelist:
        try:
            for worker in structure.wlist:
                workerlist.add(worker)
                cameralist.add(worker)

        except AttributeError:
            for unit in structure.ulist:
                unitlist.add(unit)
                cameralist.add(unit)


    pygame.display.update()
    # limits FPS to 60
    # dt is delta time in seconds since last frame, used for framerate-
    # independent physics.
    dt = clock.tick(60) / 1000

pygame.quit()